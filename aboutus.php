    <h4>Why Us</h4>
    <p>Having an on-campus farmers market transforms an element of the Cornell experience, providing students with closer ties to Ithaca and with an opportunity to purchase local, fresh foods.
      Ithaca is surrounded by farms and local foods enthusiasts, named one of America’s Foodiest Towns in 2010 by Bon Appétit, but Cornell students are somewhat disconnected from this movement.</p>
      <p>A Farmers' Market on campus allows students to interact with local citizens and business owners, which provides for a stronger relationship between Cornell students and the city of Ithaca.
        For local farmers, artisans and restaurant owners the market is an opportunity to sell their products to an untapped market: college students.
         No fresh foods markets exist in Collegetown, leaving car-less students without adequate access to healthy food.
         The Farmers’ Market at Cornell provides the opportunity to bridge the gap between farm and fork, encouraging students to think about and value their food.</p>
